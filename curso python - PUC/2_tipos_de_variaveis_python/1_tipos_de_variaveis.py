'''
Em Python, existem diferentes tipos de variáveis que você pode usar para armazenar valores de diferentes tipos de dados.
Aqui estão alguns dos tipos de variáveis mais comuns em Python:

* Inteiro : int
* Flutuante: float
* Strings: str
* Booleanos: bool

Obs: Em python não é necessário declarar explicitamente a variável. Isso chama-se Tipagem Dinâmica

'''

'''
Números inteiros (int): São usados para representar números inteiros positivos ou negativos, sem casas decimais. Por exemplo:
'''
idade = 25

'''
Números de ponto flutuante (float): São usados para representar números com casas decimais. Por exemplo:
'''
altura = 1.75

'''
Strings (str): São usados para representar sequências de caracteres.
Uma string é definida entre aspas simples ('') ou aspas duplas (""). Por exemplo:
'''
nome = "João"

'''
Booleanos (bool): Podem ter apenas dois valores: True (verdadeiro) ou False (falso).
São usados para representar estados lógicos. Por exemplo:
'''
temperatura_alta = True


'''
Obs:Nessa explicação foi usado as docstrings. As docstrings são strings de documentação usadas para documentar
funções, métodos, classes e módulos. O termo "docstring" é uma contração de "documentação" e "string".
Arquivos em python usam a extençao .py
'''