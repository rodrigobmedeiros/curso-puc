'''
Em programação, as variáveis são utilizadas para armazenar valores que podem ser
manipulados e acessados durante a execução de um programa.
'''

'''
Em Python, você pode criar uma variável atribuindo um valor a ela usando o operador de atribuição "=", 
que indica que o valor à direita será armazenado na variável à esquerda. Por exemplo:
'''
idade = 25

'''
Você também pode atribuir novos valores a uma variável já existente. 
A nova atribuição substituirá o valor anteriormente armazenado na variável. Por exemplo:
'''
idade = 25
idade = 30

'''
As atribuições também podem ser feitas com base em expressões, 
onde você combina valores e operadores para obter um novo valor. Por exemplo:
'''
x = 5
y = x + 3